// while & do while loop

// While loop
let index = 0;
while (index <= 10) {
    // console.log(`value of index ${index}`);
    index = index + 2
}

let myarr = ["Batman", "Superman", "Loki"]
let arr = 0
while (arr < myarr.length) {
    // console.log(`value is ${myarr[arr]}`);
    arr ++
}

// Do while loop

let level = 11
do {
    console.log(`level is ${level}`);
    level++
} while (level <= 10);